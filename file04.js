//rules for naming a variables

// you cannot start with number
// example :-
// 1value (invalid)
// value1 (valid)

var value1 = 5;
console.log(value1);

// you can use only underscore _ or dollar $ symbol
// first_name (valid)
// _firstname (valid)

var first_name = "Jonu";
console.log(first_name);

var _firstname = "Cherry";
console.log(_firstname);

// first$name (valid)
// $firstname (valid)

var first$name = "Monu";
console.log(first$name);

var $firstname = "Sonu";
console.log($firstname);

